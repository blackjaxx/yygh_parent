-- 查看各表数据概览
SELECT '====== yygh_hosp.hospital_set ======' AS '';
SELECT id, hosname, hoscode, status FROM yygh_hosp.hospital_set WHERE id >= 100 ORDER BY id;

SELECT '====== yygh_user.user_info ======' AS '';
SELECT id, nick_name, phone, name, auth_status, status FROM yygh_user.user_info WHERE id >= 100 ORDER BY id;

SELECT '====== yygh_user.patient ======' AS '';
SELECT id, user_id, name, certificates_no, sex, phone, is_insure, status
FROM yygh_user.patient WHERE id >= 100 ORDER BY user_id, id;

SELECT '====== yygh_order.order_info ======' AS '';
SELECT id, user_id, out_trade_no, hosname, depname, patient_name, reserve_date,
       reserve_time, amount, order_status
FROM yygh_order.order_info WHERE id >= 100 ORDER BY id;

SELECT '====== yygh_order.payment_info ======' AS '';
SELECT id, out_trade_no, order_id, payment_type, total_amount, payment_status
FROM yygh_order.payment_info WHERE id >= 100 ORDER BY id;

-- ============================================================================
-- 数据说明
-- ============================================================================
-- order_status（订单状态）:
--     0  = 待支付
--     1  = 已取消
--     2  = 已支付
--    -1  = 已取消/退款
--
-- payment_status（支付状态）:
--     0  = 未支付
--     1  = 支付中
--     2  = 已支付
--     3  = 已退款
--
-- auth_status（认证状态）:
--     0  = 未认证
--     1  = 认证中
--     2  = 认证成功
--
-- status（用户/医院状态）:
--     0  = 锁定/下线
--     1  = 正常/上线
--
-- sex（性别）:
--     0  = 女
--     1  = 男
--
-- reserve_time（预约时间）:
--     0  = 上午
--     1  = 下午
--
-- certificates_type（证件类型）:
--     '10' = 身份证
--     '20' = 户口本
-- ============================================================================